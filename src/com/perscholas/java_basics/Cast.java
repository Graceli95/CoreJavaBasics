package com.perscholas.java_basics;

public class Cast {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;
//        integer division, the result will be integer
        int q = y/x;
        System.out.println(q);
//        (double)y  cast y to a double
        double qDouble = (double)y/x;
        System.out.println(qDouble);

    }
}
